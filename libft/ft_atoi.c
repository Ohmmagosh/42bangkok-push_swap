#include "libft.h"

int	ft_atoi(const char *str)
{
	int					opt;
	long	number;

	number = 0;
	opt = 1;
	while ((*str >= 9 && *str <= 13) || (*str == 32))
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			opt *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		number *= 10;
		number += *str - 48;
		str++;
	}
	if (number > 2147483647 || number < -2147483648 )
		return (error_re(3));
	return (opt * number);
}
